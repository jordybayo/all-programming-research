#vectors

#are R-datatype divided in 6 types:
  #character "ABX"
  #double decimal value
  #Raw eg charToRaw("vai")
  #integer e.g 22L
  #logical TRUE/FALSE
  #complex e.g 2+8i

#create vectors
v1 <- c(3,4,5,6,7,8)
v2 <- c(4,5,6,7,8,9)

#addition
add.result <- v1+v2
print(add.result)

#substration
result <- v2-v1
print(result)

#multiplications
multi.result <- v1*v2
print(multi.result)

#divisions
div.result <- v2/v1
print(div.result)
